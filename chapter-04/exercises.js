////////////////////////////////////////////////////////////////////////////////

function range(x, y, z) {
  let s = [];
  if (x === y || z < 0) {
    return s;
  }
  if (x < y && z === 1 || x < y && !z) {
    for (let i = x; i <= y; i++) {
      s.push(i);
    }
    return s;
  }
  if (x < y && z > 1) {
    for (let i = x; i <= y; i+= z) {
      s.push(i);
    }
    return s;
  }
}

