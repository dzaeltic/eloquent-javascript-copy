// flatten /////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function flatten(arr) { 
let out = [];
  for(let item of arr){
     out = out.concat(item);
  }
  return out;
}

// /////////////////////////////////////////////////////////////////////////////
// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function loop(value, test, update, execute) {
  if (!test(value)) return; 
  execute(value);
  return loop(update(value), test, update, execute);
}

// /////////////////////////////////////////////////////////////////////////////
// every ///////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function every(arr, test) {
  if (arr.filter(test).length === arr.length) {
    return true;
  }
  return false;
}

