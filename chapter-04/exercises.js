////////////////////////////////////////////////////////////////////////////////
// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(x, y, z) {
  let s = [];
  if (x === y || z < 0) {
    return s;
  }
  if (x < y && z === 1 || x < y && !z) {
    for (let i = x; i <= y; i++) {
      s.push(i);
    }
    return s;
  }
  if (x < y && z > 1) {
    for (let i = x; i <= y; i+= z) {
      s.push(i);
    }
    return s;
  }
}

////////////////////////////////////////////////////////////////////////////////
// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(arr) {
  if (!arr.length){
    return 0;
  } else {
  return arr.reduce((cnt, item) => cnt += item);
  }
 }

////////////////////////////////////////////////////////////////////////////////
// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(arr) {
  return arr.toReversed();
}

////////////////////////////////////////////////////////////////////////////////
// reverseArrayInPlace /////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArrayInPlace(arr) {
  return arr.reverse();

}

////////////////////////////////////////////////////////////////////////////////
// arrayToList /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function arrayToList(arr) {
  let list = null;
  for (let i = arr.length - 1; i >= 0; i--) {
    list = {
      value: arr[i],
      rest: list
    };
  }
  return list;

}

////////////////////////////////////////////////////////////////////////////////
// listToArray /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function listToArray(list) {
  const arr = [];
  let current = list;
  while (current) {
    arr.push(current.value);
    current = current.rest;
  }
  return arr;
}

////////////////////////////////////////////////////////////////////////////////
// prepend /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function prepend(item, list) {
  let arr = listToArray(list);
  arr.unshift(item);
  return arrayToList(arr);
}

////////////////////////////////////////////////////////////////////////////////
// nth /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function nth(list, ind) {
  let arr = listToArray(list);
  return arr[ind];
}

////////////////////////////////////////////////////////////////////////////////
// deepEqual ///////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function deepEqual(x, y) {
  if ((typeof x == "object" && x != null) && (typeof y == "object" && y != null)) {
    if(Object.keys(x).length === 0 && Object.keys(y).length === 0) {
      return true;
    }
    
    if (Object.keys(x).length != Object.keys(y).length)
      return false;
    for (var prop in x) {
      if (y.hasOwnProperty(prop))
        return deepEqual(x[prop], y[prop]);
      }
    }
  else if (x !== y)
    return false;
  else
    return true;
}

